// 接下來要介紹公司面分析，有非常多個指標可以分析公司，相信在沙場上拼鬥的投資人都有一套自己的選股邏輯，首先介紹給初心者們如何評估股票的「獲利能力 (profitability)」。

// profitability 獲利能力 — 損益表 (income statement) 相關概念
// 首先要介紹會計三大報表中的損益表（income statement），裡面包含公司從全部賺到的錢到最後淨收益的過程，如果大家沒有相關概念可以先點連結（CMOENY 損益表觀念）學習哦， 小 V 也有準備精簡 + 英文搭配在「補充二」哦～

// 盈餘（稅後淨利） = 營業收入 – 營業成本 – 營業費用 – 業外收入 / 支出 – 稅務

// 盈餘（以每股盈餘 (EPS) 當指標）、營收、稅後淨利率（影響因素包含營業成本、營業費用、業外收入 / 支出、稅務），這些指標很重要的就是要看 YoY (year over year)、QoQ（quarter over quarter）、MoM (month over month) 的成長率，如果要長期投資的話，就要找到每年皆會持續成長的公司，若出現持續衰退甚至獲利是負的，建議這種股票不要碰哦：）

// dividend yield 股利率（殖利率）
// 證券投資除了賺「股價差」當然還有「股利」啊，現在就來介紹它嘍，每年 8 月左右，台灣進入發放股利的時期（俗稱：除權、除息），但股東們手上的股息從哪裡來呢？當然是公司的帳上啦～

// 因此如果現在股價 100，而股息為 2 元的話，隔天的開盤價預估應該在 100 – 2 = 98 元，但是為什麼還是這麼多人覺得發股利是賺到呢？因為多數公司皆有「填權、填息」的行情，所謂「填權息行情」，也就是將股利 2 元漲回來的意思。

// market beta 貝它指數
// 這是一個用來衡量股票風險的指標，當 beta 係數大於一，表示「該股票的報酬波動」較「整個大盤波動」大，但這個指標沒有好與壞之分，若你較喜歡短時間大漲大跌的股票，就可以考慮 beta 高一點的股票。

// capital expenditure 資本支出
// 資本支出也就是為了「獲取或延長固定資產（如：機器設備或廠房）」而產生的費用，對於一些高科技公司，這個指標算是蠻重要的，若沒有持續的創新就會被其他競爭者超越，因此大家不妨可以多關注。

